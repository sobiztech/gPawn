    {
        $id=0;
        $id=$request->id;

        $setting=new settings();
        $setting->key_name=$request->input('key_name');
        $setting->key_value=$request->input('key_value');
        $setting->description=$request->input('description');

        if($id)
        {
            $setting=settings::find($id);
            $setting->save();
        }
        else
        {
            $setting->save();
        }
        return $setting;
    }

